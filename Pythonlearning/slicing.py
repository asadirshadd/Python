#In Python, slicing is an operation that allows you to extract specific portions of
#  sequences like lists, tuples, and strings by specifying a start and ending index , 
# and step indices with the syntax sequence[start:stop:step] .

str = "United state of america"

#if i want to extract the state word from the above string so we will use slicing 

print(str[7:12])

# we can also use the negative indexing for slicing as well

